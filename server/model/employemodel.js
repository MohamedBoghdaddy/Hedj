import mongoose from 'mongoose';

const employeeSchema = new mongoose.Schema({
    fname: {
        type: String,
        required: true
    },
    lname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    department: {
        type: String,
        required: true
    }
});

export default mongoose.model('Employee', employeeSchema);
